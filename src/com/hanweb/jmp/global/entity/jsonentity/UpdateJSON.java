package com.hanweb.jmp.global.entity.jsonentity;

import java.io.Serializable;

/**
 * 客户端版本升级JSON实体
 * @author WangFei
 * 
 */
public class UpdateJSON implements Serializable {
	
	/**
	 * 序列id
	 */
	private static final long serialVersionUID = 8170862281896896666L;

	/**
	 * 服务器版本号
	 */
	private String version;

	/**
	 * 更新说明
	 */
	private String update;

	/**
	 * 更新说明流
	 */
	private String updateEdit;

	/**
	 * 一键打包app下载地址
	 */
	private String appdownurl;

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getUpdate() {
		return update;
	}

	public void setUpdate(String update) {
		this.update = update;
	}

	public String getUpdateEdit() {
		return updateEdit;
	}

	public void setUpdateEdit(String updateEdit) {
		this.updateEdit = updateEdit;
	}

	public String getAppdownurl() {
		return appdownurl;
	}

	public void setAppdownurl(String appdownurl) {
		this.appdownurl = appdownurl;
	}

}