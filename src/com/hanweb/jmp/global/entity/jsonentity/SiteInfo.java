package com.hanweb.jmp.global.entity.jsonentity;

import java.io.Serializable;

/**
 * 网站信息JSON实体
 * @author WangFei
 */
public class SiteInfo implements Serializable {
	
	/**
	 * 序列id
	 */
	private static final long serialVersionUID = 7337576688944379624L;

	/**
	 * 网站id
	 */
	private Integer siteId;

	/**
	 * 网站logo URL
	 */
	private String siteLogoUrl = "";

	/**
	 * 网站名字
	 */
	private String siteName = "";

	/**
	 * 关于本站
	 */
	private String siteAbout = "";

	/**
	 * 关于本站
	 */
	private String about = "";

	public Integer getSiteId() {
		return siteId;
	}

	public void setSiteId(Integer siteId) {
		this.siteId = siteId;
	}

	public String getSiteLogoUrl() {
		return siteLogoUrl;
	}

	public void setSiteLogoUrl(String siteLogoUrl) {
		this.siteLogoUrl = siteLogoUrl;
	}

	public String getSiteName() {
		return siteName;
	}

	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}

	public String getSiteAbout() {
		return siteAbout;
	}

	public void setSiteAbout(String siteAbout) {
		this.siteAbout = siteAbout;
	}

	public String getAbout() {
		return about;
	}

	public void setAbout(String about) {
		this.about = about;
	}

}