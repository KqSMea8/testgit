	/**
	 * interfaceResult:(返回接口的json).
	 *
	 * @param result 结果
	 * @param mode 模块编码
	 * @param errorCode 错误描述
	 * @return    设定参数 .
	*/
	public static String interfaceResult(boolean result, String mode, String errorCode){
		HashMap<String, Object> ret = new HashMap<String, Object>(); 
		String returnResult="true";
		if(!result){
			returnResult="false";
		}
		ret.put("result", returnResult);
		ret.put("modecode", mode);
		ret.put("message", errorCode);
		return JsonUtil.objectToString(ret); 
	}
