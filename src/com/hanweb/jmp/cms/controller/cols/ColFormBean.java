package com.hanweb.jmp.cms.controller.cols;

import org.springframework.web.multipart.MultipartFile;

import com.hanweb.jmp.cms.entity.cols.Col;

public class ColFormBean extends Col implements java.io.Serializable{

	private static final long serialVersionUID = -7385672236680207925L;
	
	/**
	 * 首图文件
	 */
	private MultipartFile firstPicFile;
	
	/**
	 * 图标文件
	 */
	private MultipartFile iconFile;
	
	/**
     * 应用图标文件
     */
    private MultipartFile appIconFile;
	
	/**
	 * 背景图文件
	 */
	private MultipartFile backFile;
	
	/**
	 * 互动首图背景图
	 */
	private MultipartFile actMobileFile;
	
	/**
	 * 互动Ipad背景图
	 */
	private MultipartFile actIpadFile; 
	
	/**
	 * 栏目的上一个父栏目ID（修改栏目所属栏目时使用）
	 */
	private Integer prevPid = 0;
	
	/**
	 * 互动XML图 
	 */
	private MultipartFile actXmlFile;
	
	public MultipartFile getFirstPicFile() {
		return firstPicFile;
	}
	
	public void setFirstPicFile(MultipartFile firstPicFile) {
		this.firstPicFile = firstPicFile;
	}
	
	public MultipartFile getIconFile() {
		return iconFile;
	}
	
	public void setIconFile(MultipartFile iconFile) {
		this.iconFile = iconFile;
	}
	
	public MultipartFile getBackFile() {
		return backFile;
	}
	
	public void setBackFile(MultipartFile backFile) {
		this.backFile = backFile;
	}
	
	public MultipartFile getActMobileFile() {
		return actMobileFile;
	}
	
	public void setActMobileFile(MultipartFile actMobileFile) {
		this.actMobileFile = actMobileFile;
	}
	
	public MultipartFile getActIpadFile() {
		return actIpadFile;
	}
	
	public void setActIpadFile(MultipartFile actIpadFile) {
		this.actIpadFile = actIpadFile;
	}
	
	public MultipartFile getActXmlFile() {
		return actXmlFile;
	}
	
	public void setActXmlFile(MultipartFile actXmlFile) {
		this.actXmlFile = actXmlFile;
	}
	
	public Integer getPrevPid() {
		return prevPid;
	}
	
	public void setPrevPid(Integer prevPid) {
		this.prevPid = prevPid;
	}

    public void setAppIconFile(MultipartFile appIconFile) {
        this.appIconFile = appIconFile;
    }

    public MultipartFile getAppIconFile() {
        return appIconFile;
    } 
	
}